  public class km_method_overloading {

      static void foo() {
          System.out.println("m m m good one ");
      }

      static void foo(int a) {
          System.out.println("good one " + a + "nh");
      }

      static void foo(int g, int b) {
          System.out.println("good one " + b + "nh");
          System.out.println("good one " + g + "nvv");
      }

      public static void main(String[] args) {
          // Testing the overloaded methods
          foo();
          foo(10);
          foo(20, 30);
      }
  }



